package cn.bugfish.dove_wz25.UserMannageSystem.Model;

public class Player {
    private String userid;
    private String nickname;
    private String level;
    private String chenghao;
    private String point;
    private String catfood;
    private String admin;

    // Getters and Setters
    public String getUserid() { return userid; }
    public void setUserid(String userid) { this.userid = userid; }
    public String getNickname() { return nickname; }
    public void setNickname(String nickname) { this.nickname = nickname; }
    public String getLevel() { return level; }
    public void setLevel(String level) { this.level = level; }
    public String getChenghao() { return chenghao; }
    public void setChenghao(String chenghao) { this.chenghao = chenghao; }
    public String getPoint() { return point; }
    public void setPoint(String point) { this.point = point; }
    public String getCatfood() { return catfood; }
    public void setCatfood(String catfood) { this.catfood = catfood; }
    public String getAdmin() { return admin; }
    public void setAdmin(String admin) { this.admin = admin; }
}
